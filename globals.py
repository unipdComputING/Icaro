def find_position(index: int, vectors: list) -> int:
  pos: int = index
  if pos < len(vectors) and vectors[index].id == pos:
    return pos
  pos = None
  for i, vec in enumerate(vectors):
    if index == vec.id:
      pos = i
      break
  return pos
