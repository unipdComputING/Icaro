import numpy as np

class Boundary:

  def __init__(self, node_id: int= None, set: str = None, fix: np = None, val: float = None, amp: str = None) -> None:
    self.node_id: int= node_id
    self.set: str = set
    self.fix: np = fix
    self.val: float = val
    self.amp: str = amp
  # ---------------------------------------------------------------------------
  def update_fix(self, value: int) -> None:
    """
    Aggiorna il valore del primo elemento nell'array `fix` con il valore fornito.
    """
    if isinstance(self.fix, np.ndarray):
      self.fix[0] = value
    else:
      print(f"Errore: fix non è un array NumPy ma un {type(self.fix)}")
